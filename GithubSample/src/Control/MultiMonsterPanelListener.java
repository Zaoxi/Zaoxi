package Control;

import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;

// 멀티플레이 몬스터 버튼에 대한 리스너
class MultiMonster0BtnListener extends MultiTowerListener implements ActionListener {
	
	public void actionPerformed(ActionEvent e) {
		if(!selectFlag) {
			MultiMapLabelListener.select = MultiMapLabelListener.MONSTER0;
			selectFlag = true;
		}
		else {
			MultiMapLabelListener.select = MultiMapLabelListener.NONE;
			selectFlag = false;
		}
	}
}

